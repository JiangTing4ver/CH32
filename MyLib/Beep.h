#ifndef __BEEP_H__
#define __BEEP_H__
/* 有源蜂鸣器：无法通过常规 GPIO 控制改变声音大小（音量），
            因其内置振荡电路和发声元件，工作电压固定，导通后音量即固定，仅能控制「发声 / 停止」和「发声频率（通过间歇通断模拟音调变化）
  * 无源蜂鸣器：可通过PWM（脉冲宽度调制）技术控制声音大小（音量）和音调高低
  --------------------------------------------------------------------------------------------------------------
  黑色外壳 + 通电就响 = 有源
  透明外壳 + 通电不响 + 要 PWM 驱动 = 无源
   */
#include "ch32f10x.h"

#include "Beep.h"

void BEEP_Init(void);
void BEEP_On(void);
void BEEP_Off(void);

#endif // __BEEP_H__
